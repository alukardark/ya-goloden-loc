<h2>2. Термины и принятые сокращения</h2>
<p>
 <b>Персональные данные (ПД)</b>&nbsp;— любая информация, относящаяся к прямо или косвенно определенному или определяемому физическому лицу (субъекту персональных данных).
</p>
<p>
 <b>Обработка персональных данных</b>&nbsp;— любое действие (операция) или совокупность действий (операций), совершаемых с использованием средств автоматизации или без использования таких средств с персональными данными, включая сбор, запись, систематизацию, накопление, хранение, уточнение (обновление, изменение), извлечение, использование, передачу, обезличивание, блокирование, удаление, уничтожение персональных данных.
</p>
<p>
 <b>Автоматизированная обработка персональных данных</b>&nbsp;— обработка персональных данных с помощью средств вычислительной техники.
</p>
<p>
 <b>Информационная система персональных данных (ИСПД)</b>&nbsp;— совокупность содержащихся в базах данных персональных данных и обеспечивающих их обработку информационных технологий и технических средств.
</p>
<p>
 <b>Персональные данные, сделанные общедоступными субъектом персональных данных</b>&nbsp;— ПД, доступ неограниченного круга лиц к которым предоставлен субъектом персональных данных либо по его просьбе.
</p>
<p>
 <b>Блокирование персональных данных</b>&nbsp;— временное прекращение обработки персональных данных (за исключением случаев, если обработка необходима для уточнения персональных данных).
</p>
<p>
 <b>Уничтожение персональных данных</b>&nbsp;— действия, в результате которых становится невозможным восстановить содержание персональных данных в информационной системе персональных данных и (или) в результате которых уничтожаются материальные носители персональных данных.
</p>
<p>
 <b>Оператор</b>&nbsp;— организация, самостоятельно или совместно с другими лицами организующая обработку персональных данных, а также определяющая цели обработки персональных данных, подлежащих обработке, действия (операции), совершаемые с персональными данными. Оператором является Общество с ограниченной ответственностью «Атлант», место нахождения:&nbsp;654027, Кемеровская обл, Новокузнецк г. Рыночный проезд, дом № 5, ОГРН&nbsp;1164205082580, ИНН&nbsp;4253036189, КПП&nbsp;425301001.</p>
